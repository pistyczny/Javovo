public class DataTypes {
    public static void main(String[] args) {
        System.out.println(1_000_000_000);
        System.out.println(5.5F);
        System.out.println(1.0/3.0);
        System.out.println(1.0/3.0F);
        System.out.println(1.0F/3.0F);
        System.out.println(1.0f/3.0f);
        System.out.println(10000000000000l);
        System.out.println(10000000000000L);
        System.out.println(50.01);
        System.out.println(50.000012313123534671927311);
        System.out.println(50.000012313123534671927311d);
        System.out.println(50.000012313123534671927311f);
        System.out.println('a');
        System.out.println('\u2602');
        System.out.println('b' + 'a');
        System.out.println(true);
        System.out.println(false);
        System.out.println("witaj swiecie");
        System.out.println(456);
        System.out.println("tak");

    }
}
