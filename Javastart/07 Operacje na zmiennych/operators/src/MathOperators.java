public class MathOperators {
    public static void main(String[] args) {

        double x = 10;
        double y = 4;
        int a = 23;
        int b = 2;



        System.out.println(x + y);
        System.out.println(x - y );
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(a % b);

        a += 5;
        System.out.println("a = " + a);

        a -= 5;
        System.out.println("a = " + a);

        a *= 5;
        System.out.println("a = " + a);

        a /= 5;
        System.out.println("a = " + a);







    }
}
