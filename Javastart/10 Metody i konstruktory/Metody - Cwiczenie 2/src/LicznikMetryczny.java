public class LicznikMetryczny {
    double metryNaCentymetry(double metr){
        return metr * 100;
    }


    double metryNaMilimetry(double metr){
        return  metr * 1000;
    }

    double centymetryNaMetry(double cemtymetry){
        return cemtymetry / 100;
    }

    double milimetryNaMetry(double milimetry){
        return milimetry / 1000;
    }


}
